    private void btnCreatePatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreatePatientActionPerformed
        // TODO add your handling code here:

        
        int selectedRow = createPatientTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from table.",
                    "Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Person person = (Person) createPatientTable.getValueAt(selectedRow, 0);
        if (person.getPatient() != null) {
            JOptionPane.showMessageDialog(this, "Patient already exists.", "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        JOptionPane.showMessageDialog(this, "Please enter the data below to create patient.");
        if(!txtId.getText().isEmpty()){
        count = Integer.parseInt(txtId.getText());
        }
        txtId.setText(String.valueOf(count+1));
    }//GEN-LAST:event_btnCreatePatientActionPerformed

    private void btnAddPatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPatientActionPerformed
        // TODO add your handling code here:
        int selectedRow= createPatientTable.getSelectedRow();
        Person person=(Person) createPatientTable.getValueAt(selectedRow, 0);
        
        String doctorName = txtDrName.getText();
        String pharmacyName = txtPharmacyName.getText();
        String patientID = txtId.getText();
        
        Patient patient = new Patient();
        
        patient.setId(patientID);
        patient.setDoctorName(doctorName);
        patient.setPharmacy(pharmacyName);
        
        patientDirectory.addOrUpdatePatient(patient);
        
        person.setPatient(patient);
        personDirectory.addAndUpdatePerson(person);
        JOptionPane.showMessageDialog(this, "Patient added!!", "Update", JOptionPane.INFORMATION_MESSAGE);
        
        personDirectory.deletePerson(person);
        populatePersonsTable(personDirectory.getPersonDirectory());
        
        populatePatientsTable(patientDirectory.getPatients());
        
    }//GEN-LAST:event_btnAddPatientActionPerformed


