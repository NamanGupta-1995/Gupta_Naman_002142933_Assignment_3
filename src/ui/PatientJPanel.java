    private void populatePersonsTable(ArrayList<Person> personsList) {
        DefaultTableModel model = (DefaultTableModel) createPatientTable.getModel();
        model.setRowCount(0);
        if(personsList.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "No Person's found. Please add"
                    + " Person's", "Warning", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        for (Person person : personsList) {
            Object[] row = new Object[4];
            row[0] = person;
            row[1] = person.getAge();
            row[2] = person.getHouseAddress();
            
            if(person.getPatient()!=null)
            {
                row[3] = person.getPatient().getId();
            }
            else
            {
                row[3] = "Patient Not Created";
            }
            model.addRow(row);
        }
    }
    
    private void populatePatientsTable(ArrayList<Patient> patientList){
        DefaultTableModel model = (DefaultTableModel) tblPatient.getModel();
        model.setRowCount(0);
        if(patientList.isEmpty()){
            JOptionPane.showMessageDialog(this, "No Patient found. Please add"
                    + " Patient", "Warning", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        for (Patient patient : patientList){
            Object[] row = new Object[3];
            row[0] = patient;
            row[1] = patient.getDoctorName();
            row[2] = patient.getPharmacy();
            model.addRow(row);
        }
        
    }
    
